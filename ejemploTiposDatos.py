#Ejercicio de tipos de datos

#Ejemplo de concateacion o suma de valores
numero1Cadena = '10'
print(f'Numero cadena 1 {numero1Cadena}')
numero2Cadena = '20'
print(f'Numero cadena 1 {numero2Cadena}')
resultado = numero1Cadena + numero2Cadena
print('El resultado de la concatenacionn de las cadenas {} y {} es {}'.format(numero1Cadena,numero2Cadena,resultado))

# Conversion a tipos enteros
numero1CadenaInt = int(numero1Cadena)
numero2CadenaInt = int(numero2Cadena)
resultado = numero1CadenaInt + numero2CadenaInt
print('El resultado de sumar {} + {} es = {}'.format(numero1CadenaInt,numero2CadenaInt,resultado))

