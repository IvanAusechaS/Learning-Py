# Nueva linea : \n Salto de linea
#Tabulacion : \t
#Comilla simple \' Permite incluir comillas simples en una cadena delimitada
#Comilla doble \' Permite incluir comillas doble en una cadena delimitada
#Barra invertida : \\ Permite incluir una barra invertida en la cadena

print("Hola \nmundo")
print("Hola \tmundo")
print("Hola \' mundo")
print("Hola \" mundo")
print("Hola \\mundo")